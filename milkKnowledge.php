                              牛奶中主要蛋白质为酪蛋白、磷酸钙和磷酸镁等，它们结合成小颗粒分布在牛奶中。另外，脂肪和微小的脂肪球也分布在牛奶中
                              牛奶看起来是白色是因为这种蛋白质的粒子和脂肪球对光不规则的反射
